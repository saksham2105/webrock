package bobby.foo;
public class Employee 
{
private Integer code;
private String name;
public Employee()
{
this.code=0;
this.name=null;
}
public void setCode(Integer code)
{
this.code=code;
}
public Integer getCode()
{
return this.code;
}
public void setName(String name)
{
this.name=name;
}
public String getName()
{
return this.name;
}
}